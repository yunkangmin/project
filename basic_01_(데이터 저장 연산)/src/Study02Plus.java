
public class Study02Plus {

	public static void main(String[] args) {
	//134 10000110
		System.out.println(134<<1);
		System.out.println(134<<2);
		
		System.out.println(134&2);
		System.out.println(134>>6);
		
		System.out.println('A'<<1);
		System.out.println(65<<1);
		
		System.out.println('김'+'찬');
		//정수형데이터의 곱셈원리
		System.out.println(0b111*0b100);
		//정수형 데이터의 나누기 연산의 비트처리
		System.out.println(0b111/0b100);
		//정수형 데이터의 나머지 연산의 비트 처리
		System.out.println(0b111%0b100);
	}

}
