
public class Study01 {

	public static void main(String[] args) {
		System.out.println();
		System.out.println("문자열");//문자열 데이터는 "" 로 묶어서 표기
		System.out.println(45);//정수형데이터 2진수로 처리
		System.out.println('문');//문자 문자마다 고유숫자값을 이진수로 저장
		System.out.println(45.33);//실수형데이터 부동소수점방식으로 변환후에 저장
		System.out.println(true);//논리형데이터 true는 1 false는 0으로

	}

}
